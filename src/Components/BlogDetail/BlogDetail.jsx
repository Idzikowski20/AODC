import React, { useState, useEffect } from "react";
import { useParams, Link } from "react-router-dom";
import axios from "axios";
import { Helmet } from "react-helmet-async";
import { getAuth, onAuthStateChanged } from "firebase/auth"; // Import Firebase Auth
import Header2 from "../Header/Header2";
import Footer2 from "../Footer/Footer2";
import "./BlogDetail.css";
import { FaHome, FaEdit } from "react-icons/fa";
import { FaShareAlt } from "react-icons/fa";

const BlogDetail = () => {
  const { id } = useParams();
  const [blog, setBlog] = useState(null);
  const [blogs, setBlogs] = useState([]);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState(null);
  const [isAuthenticated, setIsAuthenticated] = useState(false);

  // Pobieranie szczeg√≥≈Ç√≥w posta
  useEffect(() => {
    const fetchBlog = async () => {
      try {
        const response = await axios.get(`${import.meta.env.VITE_API_URL}/api/blogs/${id}`);
        setBlog(response.data);
      } catch (err) {
        console.error("‚ùå B≈ÇƒÖd pobierania posta:", err?.response?.data || err.message);
        setError("‚ùå Nie uda≈Ço siƒô pobraƒá posta. Sprawd≈∫ ID lub spr√≥buj p√≥≈∫niej.");
      } finally {
        setLoading(false);
      }
    };

    if (id && id.length === 24) fetchBlog();
    else {
      setError("‚ùå Nieprawid≈Çowy format ID.");
      setLoading(false);
    }
  }, [id]);

  // Pobranie listy wszystkich blog√≥w
  useEffect(() => {
    const fetchBlogs = async () => {
      try {
        const response = await axios.get(`${import.meta.env.VITE_API_URL}/api/blogs`);
        setBlogs(response.data);
      } catch (err) {
        console.error("‚ùå B≈ÇƒÖd pobierania listy blog√≥w:", err);
      }
    };

    fetchBlogs();
  }, []);

  // Sprawdzanie statusu zalogowania u≈ºytkownika przez Firebase Auth
  useEffect(() => {
    const auth = getAuth();
    const unsubscribe = onAuthStateChanged(auth, (user) => {
      setIsAuthenticated(!!user); // Je≈õli user istnieje, oznacza to, ≈ºe jest zalogowany
    });

    return () => unsubscribe(); // Cleanup funkcji
  }, []);

  // Funkcja udostƒôpniania
  const handleShare = () => {
    if (navigator.share) {
      navigator.share({
        title: blog.title,
        text: blog.content ? blog.content.substring(0, 150) + "..." : "Artyku≈Ç na blogu AODC",
        url: window.location.href,
      })
      .then(() => console.log('Udostƒôpniono!'))
      .catch((error) => console.log('B≈ÇƒÖd przy udostƒôpnianiu', error));
    } else {
      alert('Udostƒôpnianie nie jest obs≈Çugiwane przez tƒô przeglƒÖdarkƒô');
    }
  };

  if (loading) return <div className="text-center text-white">‚è≥ ≈Åadowanie...</div>;
  if (error) return <div className="text-center text-red-500">{error}</div>;
  if (!blog) return <div className="text-center text-gray-400">üôÅ Post nie istnieje</div>;

  return (
    <>
      {/* Dynamiczne ustawienie meta title */}
      <Helmet>
        <title>{blog.title ? `${blog.title} | AODC Blog` : "AODC Blog"}</title>
        <meta name="description" content={blog.content ? blog.content.substring(0, 150) + "..." : "Artyku≈Ç na blogu AODC"} />
      </Helmet>

      <Header2 />
      <div className="bluur"></div>
      <div className="bluur2"></div>

      {/* Nag≈Ç√≥wek z du≈ºym obrazem */}
      <div className="blog-header">
        <div className="blog-header-title">
            <h1 className="blog-title">{blog.title}</h1>
            <div className="blog-meta">
              <span>‚úçAutor:  {blog.author || "AODC"}</span>
              <span>üìÖOpublikowano:  {new Date(blog.createdAt).toLocaleDateString()}</span>
            </div>
        </div>
      </div>

      {/* G≈Ç√≥wna tre≈õƒá + Sidebar */}
      <div className="blog-detail">
        <div className="blog-content">
          <div className="blog-content-buttons-con">
              <div className="edit-post-back">
                  <Link to="/Blog">
                    <img src="/assets/back.png" alt="back"/>
                  </Link>
              </div>
              
              {/* Wy≈õwietlanie przycisk√≥w tylko dla zalogowanych u≈ºytkownik√≥w */}
              {isAuthenticated && (
                <div className="blog-content-adminbuttons">
                    <Link to={`/AdminPanel/edit/${blog._id}`} className="edit-btn">
                      <FaEdit /> Edytuj
                   </Link>
                    <Link to={`/AdminPanel`} className="edit-btn">
                    <FaHome /> Panel
                   </Link>
                </div>
              )}

              {/* Ikona udostƒôpniania */}
              <div className="share-button">
                <button onClick={handleShare}>
                  <FaShareAlt /> 
                </button>
              </div>
          </div>
          <p dangerouslySetInnerHTML={{ __html: blog.content }}></p>
        </div>

        <aside className="sidebar">
          <h2>Inne wpisy</h2>
          <div className="sidebar-posts">
            {blogs
              .filter((item) => item._id !== id)
              .map((item) => (
                <Link key={item._id} to={`/blog/${item._id}`} className="sidebar-post">
                  <img src={item.image || "/placeholder-image.jpg"} alt={item.title} />
                  <div>
                    <h3 className="sidebar-post-title">{item.title}</h3>
                    <span className="sidebar-post-date">{new Date(item.createdAt).toLocaleDateString()}</span>
                  </div>
                </Link>
              ))}
          </div>
        </aside>
      </div>

      <Footer2 />
    </>
  );
};

export default BlogDetail;
